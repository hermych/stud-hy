function validarDatos(datos) {
  respuesta = {};
  datos.forEach((dato) => {
    if (dato.value == "" || dato.value == "0") {
      respuesta[dato.name] = `El campo ${dato.name} es obligatorio`;
    }
    if (dato.name == "dniContacto") {
      if (dato.value.length != 8) {
        respuesta[dato.name] = `El DNI de contacto tiene que ser 8 digitos`;
      }
    }
  });
  return respuesta;
}
function validarInputSoloNumeros(evt) {
  let code = evt.which ? evt.which : evt.keyCode;
  if (code == 8) {
    // backspace.
    return true;
  } else if (code >= 48 && code <= 57) {
    // is a number.
    return true;
  } else {
    // other keys.
    return false;
  }
}
function listarDepartamentos() {
  window.$.ajax({
    type: "post",
    url: "DepartamentoController.php?method=listarDepartamentos",
    beforeSend: function () {},
    success: function (response) {
      let data = JSON.parse(response);
      let selectDepa = document.getElementById("departamento");
      contenido = "<option value='0'>Seleccion departamento</option>";
      data.forEach((depa) => {
        contenido += `<option value=${depa.id_departamento}>${depa.nombre}</option>`;
      });
      selectDepa.innerHTML = contenido;
    },
  });
}
